# This is a simple band name generator program. 
# It prompts the user to enter the name of the city they grew up in and
#  the name of their pet, and then combines those two inputs to create
#  a potential band name.
print("welcome to the band name generator.")
city_name = input("what is the name of the city you grew up in? \n")
pet_name = input("what is the name of your pet? \n")
print("your band name could be " + city_name + " " + pet_name)